---- POPULAR TIPO_SERVICO_SERVICO ----
CREATE PROCEDURE SP_POPULAR_SERVICO_TIPOSERVICO (@ID_SERVICO INT, @ID_TIPO_SERVICO INT OUTPUT)
AS
BEGIN
	DECLARE @VALOR FLOAT

	SELECT @VALOR = VALOR FROM TB_TIPO_SERVICO WHERE CODIGO = @ID_TIPO_SERVICO

	INSERT INTO TB_SERVICO_TIPOSERVICO
	VALUES (@ID_SERVICO, @ID_TIPO_SERVICO, @VALOR)
END


