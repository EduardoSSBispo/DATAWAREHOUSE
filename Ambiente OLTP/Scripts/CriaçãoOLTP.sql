CREATE DATABASE DATAWAREHOUSE
USE DATAWAREHOUSE

-- Salão
CREATE TABLE TB_SALAO(
	ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
	NOME VARCHAR(45) NOT NULL,
	CNPJ VARCHAR (15) NOT NULL UNIQUE,
	DTNASCIMENTO DATETIME NOT NULL,
	TELEFONE VARCHAR(20) NOT NULL,
	RUA VARCHAR(45) NOT NULL,
	CIDADE VARCHAR(45) NOT NULL,
	NUMERO VARCHAR(10)
)

-- Agendamento
CREATE TABLE TB_Agendamento (
    ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    horario DATETIME NOT NULL,
    status VARCHAR(11) NOT NULL CHECK(STATUS IN('AGENDADO', 'CANCELADO', 'REALIZADO')),
    idSalao INT,
    idCliente INT,
)

-- Cliente
CREATE TABLE TB_Cliente (
    id INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
    nome VARCHAR(45) NOT NULL,
    cpf VARCHAR(45) NOT NULL UNIQUE,
    telefone VARCHAR(45) NOT NULL,
    rua VARCHAR(45) NOT NULL,
    cidade VARCHAR(45) NOT NULL,
    dt_nascimento DATETIME NOT NULL,
    numero VARCHAR(10) 
)

-- Funcionário
CREATE TABLE TB_FUNCIONARIO(
	MATRICULA INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	ID_ESPECIALIZACAO INT,
	NOME VARCHAR(45) NOT NULL,
	CPF VARCHAR(45) NOT NULL UNIQUE,
	TELEFONE VARCHAR(20) NOT NULL,
	DTNASCIMENTO DATETIME NOT NULL,
	RUA VARCHAR(45) NOT NULL,
	CIDADE VARCHAR(45) NOT NULL
)

-- Serviço
CREATE TABLE TB_Servico (
    ID INT NOT NULL IDENTITY(1,1) PRIMARY KEY,
    descricao VARCHAR(200),
    idFuncionario INT,
    idAgendamento INT,
)

-- Serviço_Produto
CREATE TABLE TB_SERVICO_PRODUTO (
    CODIGO INT,
    ID INT,
	PRIMARY KEY(CODIGO, ID)
)

-- TipoServiço
CREATE TABLE TB_TIPO_SERVICO(
	CODIGO INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	SERVICO VARCHAR(45) NOT NULL
)

-- TipoServico_Servico
CREATE TABLE TB_SERVICO_TIPOSERVICO(
	ID_SERVICO INT,
	ID_TIPO_SERVICO INT,
	PRIMARY KEY(ID_SERVICO, ID_TIPO_SERVICO)
)

-- Produto
CREATE TABLE TB_PRODUTO(
	CODIGO INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	NOME VARCHAR(45) NOT NULL,
	QUANTIDADE INT NOT NULL
)

--Especialização
CREATE TABLE TB_ESPECIALIZACAO(
	ID INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	NOME VARCHAR(45) NOT NULL
)

